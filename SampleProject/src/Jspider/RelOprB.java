package Jspider;

public class RelOprB {
	public static void main(String[] args)
	{
		int a=10;
		int b=7;
		System.out.println(a);
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<(b+2));
		System.out.println(a);
		System.out.println(a++<=5);
		System.out.println(a);
		System.out.println(a++>9);
		System.out.println(a);
		System.out.println(a==5);
		System.out.println(a);
		System.out.println(b);
	}
}
