package Jspider;

public class PrimativeCasting {
	public static void main(String[] args)
	{
		int a=1;
		long l=a;//Auto Widening
		double d=l;//Auto Widening
		float f= (float)d;//Narrowing
		double d1=f;
		int a1=(int)f;//Narrowing
		System.out.println("a ="+a);
		System.out.println("l ="+l);
		System.out.println("d ="+d);
		System.out.println("d1 ="+d1);
		System.out.println("f ="+f);
		System.out.println("a1 ="+a1);
	}

}//#126 2nd floor Elegance Garnet Apartment, next Velvette Hotel, Bellandur, Bangalore.
